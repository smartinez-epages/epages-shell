package Test::Shell;
use base Shell::Shell;

use strict;

#======================================================================================================================
# §function     new
# §state        public
#----------------------------------------------------------------------------------------------------------------------
# §syntax       my $Shell = Shell->new();
#----------------------------------------------------------------------------------------------------------------------
# §description  Test shell constructor
#----------------------------------------------------------------------------------------------------------------------
# §return       $Shell | A new shell object instance | Object
#======================================================================================================================
sub new {
    my $class = shift;

    my $hOptions = $_[0] // {};

    my $hAttributes  = {
        'Commands'          => [
                                    'Test::Command::Test',
                               ]
    };

    return $class->SUPER::new({ %$hAttributes, %$hOptions });
}

1;