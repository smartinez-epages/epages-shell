#======================================================================================================================
# Clear
#======================================================================================================================
package Shell::BasicCommand::Clear;
use base Shell::Command::Command;

use strict;

#======================================================================================================================
# §function     getName
# §state        public
#======================================================================================================================
sub getName {
    my $self = shift;

    return 'clear';
}

#======================================================================================================================
# §function     getAlias
# §state        public
#======================================================================================================================
sub getAlias {
    my $self = shift;

    return [ 'cls' ];
}

#======================================================================================================================
# §function     getDescription
# §state        public
#======================================================================================================================
sub getDescription {
    my $self = shift;

    return [ 'Clears the console' ];
}

#======================================================================================================================
# §function     execute
# §state        public
#======================================================================================================================
sub execute {
    my $self = shift;
    my ($CommandArgs) = @_;

    $self->{'Shell'}->getConsole()->debug("Execute command CLEAR\n");

    system("clear");

    return;
}

1;
