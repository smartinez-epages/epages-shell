#======================================================================================================================
# Echo
#======================================================================================================================
package Shell::BasicCommand::Echo;
use base Shell::Command::Command;

use strict;

#======================================================================================================================
# §function     getName
# §state        public
#======================================================================================================================
sub getName {
    my $self = shift;

    return 'echo';
}

#======================================================================================================================
# §function     getDescription
# §state        public
#======================================================================================================================
sub getDescription {
    my $self = shift;

    return [ 'Echoes the arguments' ];
}

#======================================================================================================================
# §function     execute
# §state        public
#======================================================================================================================
sub execute {
    my $self = shift;
    my ($CommandArgs) = @_;

    my $Console = $self->{'Shell'}->getConsole();
    $Console->debug("Execute command ECHO\n");
    $Console->output("$CommandArgs\n");

    return;
}

1;
