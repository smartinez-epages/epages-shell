#======================================================================================================================
# Quit
#======================================================================================================================
package Shell::BasicCommand::Quit;
use base Shell::Command::Command;

use strict;

#======================================================================================================================
# §function     getName
# §state        public
#======================================================================================================================
sub getName {
    my $self = shift;

    return 'quit';
}

#======================================================================================================================
# §function     getAlias
# §state        public
#======================================================================================================================
sub getAlias {
    my $self = shift;

    return [ 'exit', 'q' ];
}

#======================================================================================================================
# §function     getDescription
# §state        public
#======================================================================================================================
sub getDescription {
    my $self = shift;

    return [ 'Exits the shell' ];
}

#======================================================================================================================
# §function     execute
# §state        public
#======================================================================================================================
sub execute {
    my $self = shift;
    my ($CommandArgs) = @_;

    my $Shell = $self->{'Shell'};

    $Shell->getConsole()->debug("Execute command QUIT\n");
    $Shell->exit(1);

    return;
}

1;
