#======================================================================================================================
# Pause
#======================================================================================================================
package Shell::BasicCommand::Pause;
use base Shell::Command::Command;

use strict;

#======================================================================================================================
# §function     getName
# §state        public
#======================================================================================================================
sub getName {
    my $self = shift;

    return 'pause';
}

#======================================================================================================================
# §function     getDescription
# §state        public
#======================================================================================================================
sub getDescription {
    my $self = shift;

    return [ 'sleeps for the specified seconds' ];
}

#======================================================================================================================
# §function     execute
# §state        public
#======================================================================================================================
sub execute {
    my $self = shift;
    my ($CommandArgs) = @_;

    $self->{'Shell'}->getConsole()->debug("Execute command SLEEP\n");

    my $hArguments = $self->_parseArguments($CommandArgs);

    my $Args = $hArguments->{'@'};
    if (defined $Args->[0]) {
        sleep($Args->[0]);
    }

    return;
}

1;
