sub printHeader {
    my $self = shift;

    if ( not $self->getArguments()->{'NoHeader'} ) {
        my $Console = $self->getConsole() ;
        $Console->reset() ;
        $Console->output(
            "\n%s\n%s%s\n",
            $self->{'Title'},
            $self->getHeaderText(),
            $self->getHelpText()
        ) ;
    }
    
    return;
}

