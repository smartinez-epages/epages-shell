sub exit {
    my $self = shift;

    my ( $ShowByeText ) = @_ ;
    
    if ( $ShowByeText ) {
        $self->getConsole()->output( "\nExit shell. Bye !\n\n" ) ;
    }

    $self->{'Exit'} = 1 ;

    return ;
}

